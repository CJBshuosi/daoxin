export const RULES = `【选题方法论】

一、选题的完整定义
选题 = 具体需求 + 创新解决方案。选题不等于话题。"如何挣100万"只是需求侧表达，不是选题。
完整选题必须包含：
- 需求侧：痛点强度、场景特异性、时效价值
- 供给侧：方案创新度、实施可行性、人设契合度

二、选题的双向构建路径
1. 常规路径：需求→解决方案（容易陷入惯性思维）
2. 创新路径：解决方案→倒推需求（制造认知惊喜，更容易出爆款）
3. 混合路径：已有素材重组创新

三、八种选题类型
1. 新鲜资讯型：信息差是核心壁垒，抢时效
2. 认知真相型：揭示常识背后的真相，制造"原来如此"
3. 情感深处型：触达用户未被表达的情感需求
4. 干货技巧型：场景颗粒度要细，避免抽象方法论
5. 体验分享型：情感穿透力是关键，收藏率和完播率高
6. 人设故事型：生活羡艳值驱动，引发模仿行为
7. 观点对撞型：认知重构度决定传播力，评论区争议比例是指标
8. 热点借势型：2小时初稿/6小时发布，跨平台内容迁移

四、选题验证标准
- 回归常识：你的观众看了会觉得有用/有趣吗？
- 回归用户：找一个具体的典型用户（"张小阳"），从他的视角检验
- 四维评估：有趣 / 有共鸣 / 有价值 / 有美感（至少满足两个）`;

