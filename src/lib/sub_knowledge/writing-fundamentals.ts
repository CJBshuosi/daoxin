export const RULES = `【文字语言基本功】

核心原则：短视频观众在疲惫、分心、碎片化的场景下观看，必须用直观的语言让人"看得见"。

一、直观语言 vs 抽象语言
- 抽象："我今天很孤独，感觉被世界遗弃了"（观众需要联想，费脑）
- 直观："推开门，黑暗的房间没有开灯，我站在门口很久不想迈进去，突然小猫喵了一声走过来"（画面感强，不用想）
- 判断标准：你说的话，观众能不能直接"看见"一个画面？能就是直观，不能就是抽象。

二、五种短视频写作手法
1. 具象化：把形容词和情绪替换成可视的动作和场景
2. 细节感：用具体的数字、名字、地点替代模糊描述（"一杯美式"比"一杯咖啡"有画面）
3. 口语化：说人话，朗读时顺口，去掉书面语和文绉绉的表达
4. 短句节奏：用短句制造节奏感和呼吸感，排比句增强感染力
5. 交流感：用"我、你、我们"，像跟朋友说话，不是在演讲

三、语言的场景匹配
- 移动端观看场景：用户可能边吃饭边看、边走路边看、躺床上看
- 日常直观的语言才能在这种场景被接受
- 抽象语言需要聚精会神思考，短视频场景做不到

四、文案≠文学
- 不是语言越优美越好，是越准确越好
- 不追求"写得好"，追求"说得对"——对的意思是观众听了有反应
- 好文案的标准是观众的行为（点赞/评论/转发），不是文采`;

