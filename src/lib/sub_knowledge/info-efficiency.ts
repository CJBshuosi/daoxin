export const RULES = `【信息效率思维】

核心公式：信息密度 = 信息总量 ÷ 时间
观众划走的本质原因：信息推进停滞，觉得无聊。

一、信息密度的判断标准
- 不是你觉得有没有信息量，而是你的目标观众觉得有没有
- 回归常识，回归用户——这八个字是一切的基石
- 年轻观众需要高密度（稍慢就觉得无聊），中老年观众需要适度降速

二、时间路标（让观众感知到信息在推进）
观众看视频是线性的，没有上下文，必须给明确的"推进感"。

六种时间路标：
1. 序号："第一、第二、第三"——最简单但最套路
2. 重复句式：排比句（"如果你心情不好…""如果你感觉烦躁…"）
3. 时间顺序："六点钟、七点钟、八点钟"
4. 空间顺序："东北、华中、华南、西南"
5. 维度切换："从这个角度看…从那个角度看…"
6. 形式突变：画面变黑、音乐切换、场景变化

三、结构可视化
- 让观众快速知道你的结构，他就能看到更多隐藏信息
- 整体大于部分之和——有了结构，1+1>2
- 一开始就让观众知道"接下来有几个部分"

四、形式与内容的关系
- 时间路标是"壳"，内容是"水"
- 不能只有壳没有内容（只模仿序号，里面没干货）
- 也不能只有内容没有壳（干货满满但观众感知不到推进）
- 形式和内容相互依存`;

