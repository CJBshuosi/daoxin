export const RULES = `【大纲架构思维】

核心原则：做内容像画画——先打草稿轮廓，再层层精细化，绝不从第一句话开始一行行写。

一、两种基本结构
1. 清单式（共识性）：多个平行要点共同说明一个主题
   - 适合：技巧类、盘点类、科普类内容
   - 观众感受：每个点都独立有价值，像拆礼物
   - 关键：各点之间无因果关系，但共同拼凑整体印象

2. 发展式（历史性）：观点不断自我否定、迭代升级
   - 适合：深度分析、故事型、认知颠覆类内容
   - 观众感受：跟着你一步步推导，最终恍然大悟
   - 关键：每一步都是"阶段性正确"，下一步推翻上一步得到更高认知

二、四种描述对象
所有短视频内容描述的对象只有四种：观点、人、物、事。
每种对象都可以用清单式或发展式来组织。

三、大纲先行原则
1. 先列出要讲的所有要点（像买菜清单）
2. 评估每个点：没意思的删掉，缺的补上
3. 确认整体结构合理后，再进入逐段细化
4. 绝不在大纲没定之前打磨文字细节

四、抽象→具象还原法则
- 观众通过具体细节理解世界，最后得到抽象观点
- 创作者必须反向：把抽象观点还原为具象细节
- "你妈妈很好"是抽象观点；"她每天早上五点起来给你熬粥"是具象细节
- 说什么不重要，观众感受到什么才重要`;

